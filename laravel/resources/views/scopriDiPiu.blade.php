Per poter usufruire al meglio dei nostri servizi è importante conoscere alcune nozioni di base <br>
All’ingresso vi sarà data una scheda. Non è vostra! Ha una duplice funzione: vi permette di avviare i giochi nelle varie postazioni da gioco e salva il punteggio che via via andrete a fare. Abbiamo un numero di litato di carte, ma dovrebbero bastare per tutti. Se non sarà disponibile al momento dell’arrivo vi metteremo in coda e vi serviremo quando potremo. <br>
Quando avrete finito di giocare, dovrete riconsegnare la carta così che i nostri operatori potranno caricare il vostro nuovo punteggio nei nostri sistemi e aggiornare la classifica. Inoltre, riceverete uno sconto in base a quanto avete ottenuto. In generale i prezzi consistono in 5 euro l’ora a cui si andrà ad applicare uno sconto più o meno grande in base alla fascia punteggio. Abbiamo attualmente 5 fasce: <br>
•	<=50 punti 0% <br>
•	<=100 punti 11.5% <br>
•	<=150 punti 22.5% <br>
•	<=300 punti 32.5% <br>
•	<=500 punti 42.5% <br>
•	>500 punti 52.5% <br>
Non preoccupatevi che ogni gioco è calibrato in modo tale da permettervi di raggiungere le varie fasce in egual misura. <br>
A tal proposito nella nostra ludoteca saranno presenti 4 sale. Ogni sala è adibita ad uno dei 4 generi che vi proponiamo: fps, arcade, corsa e quiz. Abbiamo un vastissimo catalogo di giochi per ogni categoria e faremo di tutto per mantenerlo aggiornato e farvi trovare sempre giochi nuovi e interessanti. <br>
In fine, ogni giorno lanceremo a sorpresa degli eventi su certi giochi che permettono di ottenere più punti per un periodo limitato andando a volte ad influire sulla difficoltà del gioco (soprattutto se parliamo di alti modificatori di punteggi). Guarda spesso la sezione eventi per non perderti nulla.  <br>
Detto  ciò non vi resta che venirci a trovare e tentare di scalare la nostra classifica!
